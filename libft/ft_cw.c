#include "libft.h"

int		ft_cw(const char *s, int c)
{
	int		i;

	i = 0;
	while (*s)
	{
		if (*s != (unsigned char)c)
		{
			++i;
			while (*s && *s != (unsigned char)c)
				++s;
		}
		else
			++s;
	}
	return (i);
}
