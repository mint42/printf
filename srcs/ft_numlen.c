#include "libft.h"

size_t	ft_numlen(long n)
{
	size_t	len;

	len = 1;
	if (n < 0)
	{
		n = n * -1;
		++len;
	}
	while (n > 9)
	{
		n = n / 10;
		++len;
	}
	return (len);
}
